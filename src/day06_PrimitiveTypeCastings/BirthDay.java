package day06_PrimitiveTypeCastings;

public class BirthDay {
    public static void main(String[] args) {
        String name = "Enes Kara",
                birthMonth = "September";
        int birthYear = 1995,
                birthDay = 9;
        System.out.println(name+" was born on "+birthDay+" "+birthMonth+" "+birthYear);
    }
}
